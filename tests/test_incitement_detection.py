"""Smoke-тесты: детектор подстрекательства к смене УК (_is_incitement_text).

Проверяем что функция:
* Ловит явные маркеры из текста и summary
* Не срабатывает на нейтральные слова без «УК»-спутника
* Не срабатывает на болтовню жильцов не про УК
"""

import pytest

from balt_dom_bot.services.pipeline import _is_incitement_text


@pytest.mark.parametrize("text,summary,expected", [
    # --- должны срабатывать ---
    ("менять ук надо, не справляются", "", True),
    ("сменить ук пора", "", True),
    ("поменять ук пора", "", True),
    ("гнать их в шею, управляющих", "", True),
    ("убрать ук из нашего дома", "", True),
    ("к чёрту такую управляющую", "", True),
    ("к черту управляющую компанию", "", True),
    ("избавиться от управляющей компании", "", True),
    ("на свалку ук", "", True),
    ("другую ук ищем", "", True),
    # summary с ключевым словом, даже если текст нейтральный
    ("ахахаха погнали", "подстрекательство к смене ук", True),
    ("", "подстрекательство к замене управляющей", True),
    # Их бот разрешает менять УК — реальный кейс из лога
    ("их бот разрешает менять ук", "", True),
    # --- НЕ должны срабатывать ---
    # нет УК-спутника
    ("снести гараж во дворе", "", False),
    ("гнать мяч в ворота", "", False),
    ("к чёрту эту погоду", "", False),
    ("избавиться от вредителей", "", False),
    ("на свалку этот диван", "", False),
    # нейтральный текст
    ("когда починят лифт?", "", False),
    ("грязно в подъезде", "", False),
    ("спасибо за ответ", "", False),
    # агрессия без упоминания смены УК
    ("мерзкие воришки", "", False),
    # пустые строки
    ("", "", False),
    ("   ", "   ", False),
])
def test_is_incitement_text(text: str, summary: str, expected: bool) -> None:
    result = _is_incitement_text(text.lower(), summary.lower())
    assert result == expected, (
        f"_is_incitement_text({text!r}, {summary!r}) = {result}, expected {expected}"
    )
